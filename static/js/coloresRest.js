function colores_1() {
  var color1 = document.getElementById("b1").innerHTML;
  
  // Banda 1
  if (color1 = "Negro") {
    $("#b1").css({"background-color":"black", "color":"white"})
  } else if (color1 = "Cafe") {
    $("#b1").css({"background-color":"#4E240F","color":"white"})
  } 
  else if (color1 = "Rojo") {
    $("#b1").css({"background-color":"red"})
  }
  else if (color1 = "Naranja") {
    $("#b1").css({"background-color":"orange"})
  }
  else if (color1 = "Amarillo") {
    $("#b1").css({"background-color":"yellow"})
  }
  else if (color1= "Verde") {
    $("#b1").css({"background-color":"green"})
  }
  if (color1 = "Azul") {
    $("#b1").css({"background-color":"blue"})
  }
  else if (color1 = "Violeta") {
    $("#b1").css({"background-color":"#A131FF"})
  }
  else if (color1 = "Gris") {
    $("#b1").css({"background-color":"gray"})
  }
  else if (color1 = "Blanco") {
    $("#b1").css({"background-color":"white"})
  }

}

